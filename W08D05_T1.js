function isLanguageDiverse(list) {
  const result = list.reduce(
    (acc, item) => {
      if (item.language === "Python") acc.Python++;
      if (item.language === "Ruby") acc.Ruby++;
      if (item.language === "JavaScript") acc.JavaScript++;

      return acc;
    },
    { Python: 0, Ruby: 0, JavaScript: 0 }
  );

  if (
    result.JavaScript / result.Python > 2 ||
    result.Python / result.JavaScript > 2
  )
    return false;

  if (
    result.JavaScript / result.Ruby > 2 ||
    result.Ruby / result.JavaScript > 2
  )
    return false;

  if (result.Python / result.Ruby > 2 || result.Ruby / result.Python > 2)
    return false;

  return true;
}

